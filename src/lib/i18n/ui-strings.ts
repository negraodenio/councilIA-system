/** Map browser locale or detectLanguage() output to a UILang */
export function resolveUILang(input?: string | null): UILang {
    // Force English UI for US/EU Market as requested by the user
    return 'English';
}

/** Get a translated string. Falls back to English if key missing. */
export function t(lang: UILang, key: string): string {
    return strings[lang]?.[key] || strings.English[key] || key;
}

/** Get the full phase labels map for a given language */
export function getPhaseLabels(lang: UILang): Record<string, string> {
    return {
        round1_analysis: t(lang, 'dc_phase_round1'),
        round2_attack: t(lang, 'dc_phase_round2'),
        round3_defense: t(lang, 'dc_phase_round3'),
        final_verdict: t(lang, 'dc_phase_verdict'),
    };
}
